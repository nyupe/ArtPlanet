public class CategorieRelationDTO
{
	public String memberNo;
	public String categorieNo;
	public String blogNo;
	
	
	public String getMemberNo()
	{
		return memberNo;
	}
	public void setMemberNo(String memberNo)
	{
		this.memberNo = memberNo;
	}
	public String getCategorieNo()
	{
		return categorieNo;
	}
	public void setCategorieNo(String categorieNo)
	{
		this.categorieNo = categorieNo;
	}
	public String getBlogNo()
	{
		return blogNo;
	}
	public void setBlogNo(String blogNo)
	{
		this.blogNo = blogNo;
	}
	
}
